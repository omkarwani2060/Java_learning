package Wrapper_Class;

public class unboxing {
    public static void main(String[] args) {

        System.out.println( "        UNBOXING      ");

        Integer a = 28;                     // int UNBOXING
        int b = a;
        System.out.println(b);

        Byte c = 84;                       // byte UNBOXING
        byte d = c;
        System.out.println(d);

        Short e = 7499;                    // short UNBOXING
        short h = e;
        System.out.println(h);

        Long i = 9374994848l;           // long UNBOXING
        long j = i;
        System.out.println(j);

        Float k = 949.98f;              //float UNBOXING
        float l = k;
        System.out.println(l);

        Double m = 98493.9498;          // double UNBOXING
        double n = m;
        System.out.println(n);

        Boolean o = true;               // Boolean UNBOXING
        boolean p = o;
        System.out.println(p);

        Character q = 'a';              // char UNBOXING
        char r = q;
        System.out.println(r);
    }

}
