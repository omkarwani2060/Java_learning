package Array_3;

import java.util.Arrays;

public class d_ar {
    public static void main(String[] args) {
        int[][] ad = {{1,3,2},{1 , 7,5,3,5}};

        for(int i=0; i< ad.length; i++){
            for (int j = 0; j<ad[i].length; j++){
                System.out.print(ad[i][j] + " ");
            }
            System.out.println();
        }

    }

}
